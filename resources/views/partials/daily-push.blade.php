(function () {
  /* ── Thumbnail auto-fetch via TikTok oEmbed ── */
  document.querySelectorAll('.dp-card[data-tiktok-url]').forEach(function (card) {
    var imgEl = card.querySelector('.dp-thumb img');
    var url   = card.dataset.tiktokUrl;
    fetch('https://www.tiktok.com/oembed?url=' + encodeURIComponent(url))
      .then(function (r) { return r.ok ? r.json() : Promise.reject(); })
      .then(function (d) { if (d.thumbnail_url) imgEl.src = d.thumbnail_url; })
      .catch(function () { /* fallback image stays */ });
  });

  /* ── Modal helpers ── */
  var modal   = document.getElementById('dpTikTokModal');
  var frame   = document.getElementById('dpTikTokFrame');
  var titleEl = document.getElementById('dpTikTokTitle');

  function openDpModal(videoId, title) {
    frame.src        = 'https://www.tiktok.com/embed/v2/' + videoId;
    titleEl.innerHTML = title;
    modal.hidden     = false;
    document.body.style.overflow = 'hidden';
    modal.querySelector('.dp-tt-close').focus();
  }

  function closeDpModal() {
    modal.hidden = true;
    frame.src    = '';
    document.body.style.overflow = '';
  }

  /* ── Card click / keyboard ── */
  document.querySelectorAll('.dp-card').forEach(function (card) {
    function handleActivate() {
      if (card.dataset.videoId) {
        openDpModal(card.dataset.videoId, card.dataset.title);
      } else if (card.dataset.href) {
        window.open(card.dataset.href, '_blank', 'noopener,noreferrer');
      }
    }
    card.addEventListener('click', handleActivate);
    card.addEventListener('keydown', function (e) {
      if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); handleActivate(); }
    });
  });

  /* ── Close triggers ── */
  modal.querySelector('.dp-tt-backdrop').addEventListener('click', closeDpModal);
  modal.querySelector('.dp-tt-close').addEventListener('click', closeDpModal);
  document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape' && !modal.hidden) closeDpModal();
  });
})();
</script>
